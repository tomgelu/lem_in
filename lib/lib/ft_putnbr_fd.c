#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long n;

	n = (long)nb;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -1 * n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + 48, fd);
}
