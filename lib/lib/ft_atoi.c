#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int neg;
	int ret;

	i = 0;
	ret = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		neg = (-(str[i] - 44) > 0 ? 1 : -1);
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		ret = ret * 10 + (int)str[i] - 48;
		i++;
	}
	return (neg * ret);
}
